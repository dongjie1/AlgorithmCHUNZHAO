class Solution {


    /**
     * @param Integer[] $nums
     * @return Integer
     * æ—¶é—´å¤æ‚åº¦: O(n)
     * ç©ºé—´å¤æ‚åº¦: O(1)
     */
    function removeDuplicates(&$nums)
    {
        $res = 1;
        $len = count($nums);
        if ($len == 1) {
            return $res;
        }

        $last = null;
        foreach ($nums as $i => $num) {
            if ($last === $num) {
                unset($nums[$i]);
                continue;
            }
            $last = $num;
            $res++;
        }

        return $res;
    }

    /**
     * ç”¨å‰åä¸¤æŒ‡é’ˆ
     * @param $nums
     * @return int
     */
    function removeDuplicates2(&$nums){
        if(empty($nums)) return 0;
        $p = 0;
        $q = 1;
        while ($q < count($nums)){
            if($nums[$q] !== $nums[$p]){
                $p++;
                $nums[$p] = $nums[$q];
            }
            $q++;
        }
        return $p;
    }
